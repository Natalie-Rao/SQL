WITH TEMP AS (SELECT H.HACKER_ID,NAME,COUNT(CHALLENGE_ID) AS CHALLENGES_CREATED 
             FROM HACKERS H JOIN CHALLENGES C ON H.HACKER_ID = C.HACKER_ID
             GROUP BY H.HACKER_ID,H.NAME)
             
SELECT HACKER_ID,NAME, CHALLENGES_CREATED
FROM TEMP
WHERE CHALLENGES_CREATED = (SELECT MAX(CHALLENGES_CREATED) FROM TEMP)
OR CHALLENGES_CREATED IN (SELECT CHALLENGES_CREATED FROM TEMP GROUP BY CHALLENGES_CREATED HAVING COUNT(*)=1)
ORDER BY CHALLENGES_CREATED DESC,HACKER_ID