SELECT CONTESTS.CONTEST_ID,CONTESTS.HACKER_ID,CONTESTS.NAME,
       SUM(TOTAL_SUBMISSIONS),
       SUM(TOTAL_ACCEPTED_SUBMISSIONS),
       SUM(TOTAL_VIEWS),
       SUM(TOTAL_UNIQUE_VIEWS)
       
FROM CONTESTS LEFT JOIN COLLEGES 
              ON CONTESTS.CONTEST_ID = COLLEGES.CONTEST_ID
              LEFT JOIN CHALLENGES 
              ON COLLEGES.COLLEGE_ID = CHALLENGES.COLLEGE_ID
              LEFT JOIN (SELECT CHALLENGE_ID,SUM(TOTAL_SUBMISSIONS) AS TOTAL_SUBMISSIONS,                                   SUM(TOTAL_ACCEPTED_SUBMISSIONS) AS TOTAL_ACCEPTED_SUBMISSIONS
                        FROM SUBMISSION_STATS GROUP BY CHALLENGE_ID ) AS S
              ON CHALLENGES.CHALLENGE_ID = S.CHALLENGE_ID
              LEFT JOIN (SELECT CHALLENGE_ID,SUM(TOTAL_VIEWS) AS TOTAL_VIEWS,                                               SUM(TOTAL_UNIQUE_VIEWS) AS TOTAL_UNIQUE_VIEWS
                        FROM VIEW_STATS GROUP BY CHALLENGE_ID ) AS V
              ON CHALLENGES.CHALLENGE_ID = V.CHALLENGE_ID
              
GROUP BY CONTESTS.CONTEST_ID,CONTESTS.HACKER_ID,CONTESTS.NAME
HAVING (SUM(TOTAL_SUBMISSIONS)+SUM(TOTAL_ACCEPTED_SUBMISSIONS)+SUM(TOTAL_VIEWS)+SUM(TOTAL_UNIQUE_VIEWS))>0
ORDER BY CONTESTS.CONTEST_ID